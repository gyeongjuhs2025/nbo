# 소득액, 소득공제액, 세액감면액 입력받기
# income = int(input("소득액(원): "))
# deduction = int(input("소득공제액(원): "))
# tax_credit = int(input("세액감면액(원): "))
income, deduction, tax_credit = map(int, input("소득액 소득공제액 세액감면액을 입력하세요: ").split())
# 1) 과세표준 계산
tax_base = income - deduction
# print("과세표준:", tax_base, "원")

# 세금 계산 변수 초기화
tax = 0

# 2) 과세표준이 0 이하이면 세금 없음
if tax_base <= 0:
    print("과세표준이 0 이하이므로 세금이 없습니다.")
else:
    # 구간별 소득세 계산
    if tax_base <= 12000000:
        tax += tax_base * 0.06

    elif tax_base <= 46000000:
        tax += 12000000 * 0.06
        tax += (tax_base - 12000000) * 0.15

    elif tax_base <= 88000000:
        tax += 12000000 * 0.06
        tax += (46000000 - 12000000) * 0.15
        tax += (tax_base - 46000000) * 0.24

    elif tax_base <= 150000000:
        tax += 12000000 * 0.06
        tax += (46000000 - 12000000) * 0.15
        tax += (88000000 - 46000000) * 0.24
        tax += (tax_base - 88000000) * 0.35

    elif tax_base <= 300000000:
        tax += 12000000 * 0.06
        tax += (46000000 - 12000000) * 0.15
        tax += (88000000 - 46000000) * 0.24
        tax += (150000000 - 88000000) * 0.35
        tax += (tax_base - 150000000) * 0.38

    elif tax_base <= 500000000:
        tax += 12000000 * 0.06
        tax += (46000000 - 12000000) * 0.15
        tax += (88000000 - 46000000) * 0.24
        tax += (150000000 - 88000000) * 0.35
        tax += (300000000 - 150000000) * 0.38
        tax += (tax_base - 300000000) * 0.40

    else:
        tax += 12000000 * 0.06
        tax += (46000000 - 12000000) * 0.15
        tax += (88000000 - 46000000) * 0.24
        tax += (150000000 - 88000000) * 0.35
        tax += (300000000 - 150000000) * 0.38
        tax += (500000000 - 300000000) * 0.40
        tax += (tax_base - 500000000) * 0.42

# 3) 세액감면 적용
final_tax = tax - tax_credit

# 최종 소득세액은 0원 이상이어야 함
if final_tax < 0:
    final_tax = 0

print("최종 소득세액:", int(final_tax), "원")
